function ocultar () 
{
	//capturo en una variable el div y lo oculto
	contenedor = document.getElementById('caja');
	contenedor.style.display = 'none';
}

function mostrar () 
{
	//capturo en una variable el div y lo muestro
	contenedor = document.getElementById('caja');
	contenedor.style.display = 'block';
}